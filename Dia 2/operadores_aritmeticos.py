print('*** Operadores Aritmeticos ***')
a, b = 10, 5
print(f'Valor inicial de a: {a}')
print(f'Valor inicial de b: {b}')

# Operador suma +
resultado = a + b
print(f'El resultado de operador suma a + b es: {resultado}')

# Operador resta
resultado = a - b
print(f'El resultado de operador resta a - b es: {resultado}')

# Operador multiplicacion *
resultado = a * b
print(f'El resultado de operador multiplicacióm a * b es: {resultado}')

# Operador división /
resultado = a / b
print(f'El resultado de operador división a / b es: {resultado}')

# Operador modulo %
resultado = a % b
print(f'El resultado de operador modulo a % b es: {resultado}')

# Operador exponenciación **
resultado = a ** b
print(f'El resultado de operador exponenciación a ** b es: {resultado}')
